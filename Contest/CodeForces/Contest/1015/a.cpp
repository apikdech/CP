#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main() {
	int n, m, cnt[105];
	memset(cnt, 0, sizeof cnt);
	cin >> n >> m;
	for (int i = 0; i < n; i++) {
		int l, r;
		cin >> l >> r;
		for (int j = l; j <= r; j++) cnt[j]++;
	}
	vector<int> v;
	for (int i = 1; i <= m; i++) {
		if (cnt[i] == 0) v.push_back(i);
	}
	cout << v.size() << "\n";
	for (int i = 0; i < v.size(); i++) {
		cout << v[i];
		if (i != v.size() - 1)cout << " ";
		else cout << "\n";
	}
	return 0;
}