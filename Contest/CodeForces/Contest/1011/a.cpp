#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main() {
	int n, k, sum = 0;
	cin >> n >> k;
	vector<int> word, ans;
	for (int i = 0; i < n; i++) {
		char x;
		cin >> x;
		word.push_back(x-'a'+1);
	}
	sort(word.begin(), word.end());
	word.erase(unique(word.begin(), word.end()), word.end());
	for (int i = 0; i < word.size(); i++) {
		if (i == 0) {
			sum += word[i];
			ans.push_back(word[i]);
		}
		else if(word[i] - ans[ans.size() - 1] > 1) {
			sum += word[i];
			ans.push_back(word[i]);
		}
		if (ans.size() == k) return cout << sum << "\n", 0;
	}
	if (ans.size() < k) cout << "-1\n";
	return 0;
}