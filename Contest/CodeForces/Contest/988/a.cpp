#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main() {
	int n, k, a[105];
	memset(a, 0, sizeof a);
	cin >> n >> k;
	vector<int> v;
	for (int i = 0; i < n; i++) {
		int x;
		cin >> x;
		if (a[x] == 0) {
			v.push_back(i+1);
		}
		a[x] = 1;
	}
	if (v.size() < k) return cout << "NO\n", 0;
	else cout << "YES\n";
	for (int i = 0; i < k; i++) cout << v[i] << " ";
	return 0;
}