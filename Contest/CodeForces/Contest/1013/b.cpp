#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main() {
	int n, x, cnt[100005], num, ans = -1, a[100005];
	cin >> n >> x;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
		cnt[a[i]]++;
		if (cnt[a[i]] == 2) return cout << "0\n", 0;
	}
	for (int i = 0; i < n; i++) {
		if (a[i] > x && cnt[(a[i]&x)] == 1) return cout << "1\n", 0;
		cnt[(a[i]&x)]++;
		if (cnt[(a[i]&x)] > 1) return cout << "2\n", 0;
	}
	cout << "-1\n";
	return 0;
}